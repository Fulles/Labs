'''Первая функция'''
g = (3*(-3*a**2+2*a*x+21*x**2))/(35*a**2+37*a*x+6*x**2)
print("g= %.3f" % g)
'''Вторая функция'''
f = 1/(math.cos(3*a**2+5*a*x+2*x**2))
print("f= %.3f" % f)
'''Третья функция'''
y = math.acosh(-12*a**2-4*a*x+x**2+1)
print("y= %.3f" % y)


